package images

// Screen dimensions for MSX screen formats
const (
	// ScreenWidth is the standard MSX screen width in pixels (256)
	ScreenWidth = 256

	// ScreenWidth7 is the width for Screen 7 (512)
	ScreenWidth7 = 512

	// ScreenHeightMSX1 is the MSX 1 screen height in pixels (192)
	ScreenHeightMSX1 = 192

	// ScreenHeightMSX2 is the MSX 2 screen height in pixels (212)
	ScreenHeightMSX2 = 212
)

// Memory addresses and offsets for MSX screen formats
const (
	// PaletteOffset5 is the palette offset for Screen 5 (0x7680)
	PaletteOffset5 = 0x7680

	// PaletteOffset is the standard palette offset (0xFA80)
	PaletteOffset = 0xFA80
)

